static uint32_t
b4525_zip(uint32_t u32A, uint32_t u32B)
{
	uint32_t u32R;
	unsigned uI;
	uint8_t u8Na;
	uint8_t u8Nb;
	uint8_t u8Idx;

	u32A &= 0x0000ffffu;
	u32B &= 0x0000ffffu;
	u32R = 0u;
	for (uI = 0u; uI < 4u; uI++) {
		u8Na = (uint8_t)((u32A >> (4u * uI)) & 0x0fu);
		u8Nb = (uint8_t)((u32B >> (4u * uI)) & 0x0fu);
		u8Idx = (uint8_t)(u8Na | (uint8_t)(u8Nb << 4));
		u32R |= (uint32_t)b4525_pair_lut[u8Idx] << (8u * uI);
	}
	return u32R;
}

