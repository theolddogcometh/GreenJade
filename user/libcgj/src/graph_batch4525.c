/*
 * gj_u32_zip_nibbles_u - interleave nibbles of two low-16 sources.
 *
 * a: even-nibble source (result nibbles 0,2,4,6)
 * b: odd-nibble source  (result nibbles 1,3,5,7)
 *
 * Returns the nibble-zipped 32-bit word. No parent wires.
 */
uint32_t
gj_u32_zip_nibbles_u(uint32_t u32A, uint32_t u32B)
{
	(void)NULL;
	return b4525_zip(u32A, u32B);
}

